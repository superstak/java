package ex1028;

public class Ex03_변수 {

	public static void main(String[] args) {

		// Ctrl + D : 줄 삭제
		// Ctrl + Alt + 방향키(위, 아래) : 줄 복사
		// Alt + 방향키(위, 아래) : 줄 이동
		// Ctrl + F : replace
		// Ctrl + Shift + F : 코드 정렬

		int number = 200;

		System.out.println(number + 2); // ; -> 코드 하나별로 컴파일 하는데 코드를 구분해주는 구분자
		System.out.println(number - 2);
		System.out.println(number * 2);
		System.out.println(number / 2);

	}

}
